    private void btnDodajStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajStavkuActionPerformed

        if (txtCena.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti cenu usluge!");
            return;
        }

        Usluga u = (Usluga) cmbUsluga.getSelectedItem();
        double cena = Double.parseDouble(txtCena.getText());

        StavkaTermina st = new StavkaTermina(null, -1, cena, u);

        ModelTabeleStavke tm = (ModelTabeleStavke) tblStavke.getModel();

        if (tm.postojiUsluga(u)) {
            JOptionPane.showMessageDialog(this, "Vec ste uneli ovu uslugu za ovaj termin!");
            return;
        }

        tm.dodajStavku(st);

        ukupnaCena = tm.vratiUkupnuCenu();

        txtUkupnaCena.setText(String.valueOf(ukupnaCena) + "din");

    }//GEN-LAST:event_btnDodajStavkuActionPerformed

    private void btnObrisiStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiStavkuActionPerformed

        int row = tblStavke.getSelectedRow();

        if (row >= 0) {
            ModelTabeleStavke tm = (ModelTabeleStavke) tblStavke.getModel();
            tm.obrisiUslugu(row);

            ukupnaCena = tm.vratiUkupnuCenu();

            txtUkupnaCena.setText(String.valueOf(ukupnaCena) + "din");

        }

    }//GEN-LAST:event_btnObrisiStavkuActionPerformed

    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed

        try {
            if (txtDatumVreme.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Morate uneti datum i vreme termina!");
                return;
            }

            Klijent k = (Klijent) cmbKlijent.getSelectedItem();
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy HH:mm");
            Date datumVreme = sdf.parse(txtDatumVreme.getText());

            ModelTabeleStavke tm = (ModelTabeleStavke) tblStavke.getModel();

            Termin t = new Termin(null, datumVreme, ukupnaCena, k, ulogovani, tm.getLista());

            KlijentKontroler.getInstance().addTermin(t);
            resetujFormu();
            JOptionPane.showMessageDialog(this, "Uspesno sacuvan termin!");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
//            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnSacuvajActionPerformed

